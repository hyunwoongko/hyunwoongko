# # 진법의 개념과 변환

# ### 10진법 → R진법
# 변환하려는 진법으로 계속 나눈 나머지들이 R진법 수이다. 나눌 숫자가 0이 되면 계산을 끝내고 아래부터 거꾸로 읽으면 그게 2진수로 변환된 것이다.
# 
# #### 10진법인 17을 2진법으로 변환
# - $17 = 2 \times 8 + 1$ 
# - $8 = 2 \times 4 + 0$ 
# - $4 = 2 \times 2 + 0$ 
# - $2 = 2 \times 1 + 0$ 
# - $1 = 2 \times 0 + 1$ 
# - 정답 : $17_{10} \to 10001_{2}$
# 
# #### 10진법인 49을 16진법으로 변환
# - $49 = 16 \times 3 + 1$ 
# - $3 = 16 \times 0 + 3$ 
# - 정답 : $49_{10} \to 31_{16}$

# ### 10진법 → R진법 (소숫점)
# 변환하려는 숫자에 계속해서 R을 곱하고 가수부를 떼어내서 기록하며, 소수부가 0이 되면 종료 떼어낸 가수부를 위에서부터 순서대로 읽으면 됨.
# 
# #### 10진법인 0.625을 2진법으로 변환
# - $0.625 \times 2 = 1.25 = 0.25 + 1$ 
# - $0.250 \times 2 = 0.50 = 0.50 + 0$ 
# - $0.500 \times 2 = 1.00 = 0.00 + 1$ 
# - 정답 : $0.625_{10} \to 0.101_2$
# 
# #### 10진법인 0.625을 16진법으로 변환
# - $0.625 \times 16 = 10.0 = 10(A) + 0$ 
# - 정답 : $0.625_{10} \to 0.A_{16}$
# 
# #### 10진법인 0.8을 16진법으로 변환
# - $ 0.8 \times 16 = 12.8 = 12(C) + 0.8 $ 
# - $ 0.8 \times 16 = 12.8 = 12(C) + 0.8 $ 
# - $ 0.8 \times 16 = 12.8 = 12(C) + 0.8 $ 
# - $ 0.8 \times 16 = 12.8 = 12(C) + 0.8 $ 
# - $ 0.8 \times 16 = 12.8 = 12(C) + 0.8 $ 
# - 정답 : 무한소수$0.8_{10} \to 0.CCCCCC..._{16}$

# ### R진법 → 10진법 (소수도 동일)
# 자릿수마다 R의 n승을 곱해서 더해주면 된다.
# 
# #### 2진법인 0100 0011을 10진법으로 변환
# - $0100 = 0\times2^7 + 1\times2^6 + 0\times 2^5 + 0\times2^4$ 
# - $0011 = 0\times2^3 + 0\times2^2 + 1\times 2^1 + 1\times2^0$ 
# - 정답 = 1 + 2 + 64 = 67
# 
# #### 2진법인 1011.0101을 10진법으로 변환
# - $1011 = 1\times2^3 + 0\times2^2 + 1\times 2^1 + 1\times2^0$ 
# - $0101 = 0\times2^{-1} + 1\times2^{-2} + 0\times2^{-3} + 1\times2^{-4}$ 
# - 정답 = 8 + 2 + 1 + 0.25 + 0.0625 = 11.3125
# 
# #### 16진법인 2A7을 10진법으로 변환
# - $2A7 = 7\times16^2 + A\times16^1 + 2\times16^0$ 
# - 정답 = 1792 + 160 + 2 = 1954
# 
# #### 16진법인 C7.AB를 10진법으로 변환
# - $C7 = C\times16^1 + 7\times16^0$ 
# - $AB = A\times16^{-1} + B\times16^{-2}$ 
# - 정답 = 192 + 7 + 0.625 + 0.04296875 = 199.66796875

# ### Bit Grouping (R1진법 → R2진법)
# 원래는 R1진법에서 R2진법으로 넘어가려면 중간에 10진법을 거쳐야함. R1진법 → 10진법 → R2진법처럼 거쳐야하는데 이는 매우 불편한 과정임. R1과 R2가 배수관계라면 비트그룹핑, 즉 끊어읽기로 매우 편하게 전환 가능
# 
# #### 2진법인 001010을 8진법으로 변환
# - $2^3 = 8$ (3자리씩 끊어서 읽기) 
# - $001010 = 001 \; 010$ 
# - $001 = 1 \; / \; 010 = 2$ 
# - 정답 : $001 \; 010_2 \to 12_8$
# 
# #### 2진법인 0010 1010을 16진법으로 변환
# - $2^4 = 8$ (4자리씩 끊어서 읽기) 
# - $0010 1010 = 0010 \; 1010$
# - $0010 = 2 \; / \; 1010 = A$
# - 정답 : $0010 \; 1010_2 = 2A_{16}$
# 
# #### 16진법인 24A을 2진법으로 변환
# - $2^4 = 8$ (4자리씩 끊어서 만들기) 
# - $24A = 0010 \; 0100 \; 1010$ 
# - 정답 : $24A_{16} = 0010 \; 0100 \; 1010_2$
# 
# #### 16진법인 24A을 8진법으로 변환
# - $001001001010 = 0010 \; 0100 \; 1010$ 
# - $001001001010 = 001 \; 001 \; 001 \; 010$ 
# - $001 \; 001 \; 001 \; 010 = 1112$ 
# - 정답 : $24A_{16} = 1112_8$ 

